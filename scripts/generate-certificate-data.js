const fs = require('fs');
const path = require('path');
const XLSX = require('xlsx');

const rootDir = path.resolve(__dirname, '..');
const frontendDir = path.join(rootDir, 'frontend');
const workbookCandidates = [
  'Premium Final Submission.xlsx',
  'Premium Final Submission Form (Responses).xlsx',
];
const outputPath = path.join(frontendDir, 'certificate-verification-data.js');

const columnAliases = {
  studentName: ['full name', 'student name', 'name'],
  internId: ['intern id', 'intern_id'],
  domainName: ['domain name', 'internship domain', 'domain'],
  domainId: ['domain id', 'domain_id'],
  package: ['package', 'package selected', 'internship package'],
  completedStatus: ['completed status', 'completion status', 'status'],
};

function normalizeHeader(value) {
  return String(value || '')
    .trim()
    .replace(/\s+/g, ' ')
    .toLowerCase();
}

function findWorkbookPath() {
  for (const fileName of workbookCandidates) {
    const candidate = path.join(frontendDir, fileName);
    if (fs.existsSync(candidate)) {
      return candidate;
    }
  }

  throw new Error(
    `Certificate verification workbook not found. Expected one of: ${workbookCandidates.join(', ')}`
  );
}

function findHeaderRow(rows) {
  return rows.findIndex((row) => {
    return row.some((cell) => normalizeHeader(cell) === 'intern id');
  });
}

function buildColumnIndex(headers) {
  const normalizedHeaders = headers.map(normalizeHeader);

  return Object.fromEntries(
    Object.entries(columnAliases).map(([fieldName, aliases]) => {
      const index = normalizedHeaders.findIndex((header) => aliases.includes(header));

      if (index === -1) {
        throw new Error(
          `Missing required certificate verification column: ${aliases[0]}`
        );
      }

      return [fieldName, index];
    })
  );
}

function cellText(value) {
  return String(value || '').trim();
}

function loadCertificateRecords(workbookPath) {
  const workbook = XLSX.readFile(workbookPath, {
    cellDates: false,
    raw: false,
  });
  const sheetName = workbook.SheetNames[0];
  const worksheet = workbook.Sheets[sheetName];
  const rows = XLSX.utils.sheet_to_json(worksheet, {
    header: 1,
    defval: '',
    blankrows: false,
  });
  const headerRowIndex = findHeaderRow(rows);

  if (headerRowIndex === -1) {
    throw new Error('Unable to find the Intern ID header row in the certificate workbook.');
  }

  const columnIndex = buildColumnIndex(rows[headerRowIndex]);

  return rows.slice(headerRowIndex + 1)
    .map((row) => ({
      studentName: cellText(row[columnIndex.studentName]),
      internId: cellText(row[columnIndex.internId]).toUpperCase(),
      domainName: cellText(row[columnIndex.domainName]),
      domainId: cellText(row[columnIndex.domainId]),
      package: cellText(row[columnIndex.package]),
      completedStatus: cellText(row[columnIndex.completedStatus]),
      issuedBy: 'Megthiran Internship Program',
    }))
    .filter((record) => record.internId);
}

function writeCertificateData(records) {
  const fileContents = [
    '// This file is generated by scripts/generate-certificate-data.js from the local Excel workbook.',
    '// Do not edit student records here; update the workbook and rebuild instead.',
    `window.MEGTHIRAN_CERTIFICATE_RECORDS = ${JSON.stringify(records, null, 2)};`,
    '',
  ].join('\n');

  fs.writeFileSync(outputPath, fileContents, 'utf8');
}

const workbookPath = findWorkbookPath();
const records = loadCertificateRecords(workbookPath);
writeCertificateData(records);

console.log(
  `Generated ${path.relative(rootDir, outputPath)} from ${path.relative(rootDir, workbookPath)} (${records.length} records).`
);
